	function showDialog() {
		var gridHtml, x, y, win;

		function getParentTd(elm) {
			while (elm) {
				if (elm.nodeName == 'TD') {
					return elm;
				}

				elm = elm.parentNode;
			}
		}

		gridHtml = '<table role="presentation" cellspacing="0" class="mce-icons-social"><tbody>';

		var width = 12;
		var height = Math.ceil(icon.length / width);
		for (y = 0; y < height; y++) {
			gridHtml += '<tr>';

			for (x = 0; x < width; x++) {
				var index = y * width + x;
				if (index < icon.length) {
					var chr = icon[index];

					gridHtml += '<td title="' + chr[1] + '"><div tabindex="-1" title="' + chr[1] + '" role="button">' +
						chr[0] + '</div></td>';
				} else {
					gridHtml += '<td />';
				}
			}

			gridHtml += '</tr>';
		}

		gridHtml += '</tbody></table>';

		var iconPanel = {
			type: 'container',
			html: gridHtml,
			onclick: function(e) {
				e.preventDefault();
				var target = e.target;
				if (/^(A)$/.test(target.nodeName)) {
					if (getParentTd(target).firstChild) {
						// editor.execCommand('mceInsertContent', false, tinymce.trim('&nbsp;' + target.outerHTML + '&nbsp;'));
						// editor.execCommand('mceInsertContent', false, '&nbsp;' + target.outerHTML + '&nbsp;');
						// editor.execCommand('mceInsertContent', false, target.outerHTML + '&nbsp;');
						editor.execCommand('mceInsertContent', false, tinymce.trim(target.outerHTML + '&nbsp;'));
						// console.log(target.outerHTML);

						if (!e.ctrlKey) {
							win.close();
						}
					}
				}
			},
		};

		win = editor.windowManager.open({
			title: "Template Social Icons",
			spacing: 10,
			padding: 10,
			style: 'max-height: 98%; overflow-y: auto; overflow-x: hidden;',
			items: [
				iconPanel,
				{
					type: 'container',
					layout: 'flex',
					direction: 'column',
					align: 'center',
					spacing: 5,
					minWidth: 0,
					minHeight: 0,
					items: [
						{
							type: 'label',
							name: 'preview',
							text: ' ',
							style: 'display:none;',
							border: 0,
							minWidth: 0,
							minHeight: 0
						},
						{
							type: 'label',
							name: 'previewTitle',
							text: ' ',
							style: 'display:none;',
							border: 0,
							minWidth: 0,
							minHeight: 0
						}
					]
				}
			],
			buttons: [
				{text: "Close", onclick: function() {
					win.close();
				}}
			]
		});
	}

