<h4> What about Hormones, Antibiotics? Is it Free Range, Organic?</h4>
<p>There is just so much marketing jargon out there and it can be confusing knowing what to prioritize when making a healthy and responsible purchasing decision. Our advice? Always ask for ethically raised. Ethically raised by definition means that an animal is raised in its natural environment, eating its natural diet, free from any additives, hormones and any GMO feed. Do not prioritize the organic label as it is just a certificate that you pay extra for. Our promise is ethically raised. We go to great lengths to ensure this at the source.&nbsp;</p>

